import UIKit

extension UIColor {
  static func mainBlack() -> UIColor {
    return #colorLiteral(red: 0, green: 0, blue: 0, alpha: 1)
  }
  
  static func lineColor() -> UIColor {
    return #colorLiteral(red: 0.7309085727, green: 0.733900249, blue: 0.7416167259, alpha: 1)
  }
  
  static func mainGray() -> UIColor {
    return #colorLiteral(red: 0.9360449314, green: 0.9360449314, blue: 0.9360449314, alpha: 1)
  }
  
  static func mainWhite() -> UIColor {
    return #colorLiteral(red: 1, green: 1, blue: 1, alpha: 1)
  }
  
  static func mainYellow() -> UIColor {
    return #colorLiteral(red: 0.9529411793, green: 0.6862745285, blue: 0.1333333403, alpha: 1)
  }
}
