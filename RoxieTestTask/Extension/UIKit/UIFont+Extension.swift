import UIKit

extension UIFont {
  
  static func helvetica17Bold() -> UIFont? {
    return UIFont(name: "Helvetica Neue Bold", size: 17)
  }
  
  static func helvetica19Bold() -> UIFont? {
    return UIFont(name: "Helvetica Neue Bold", size: 19)
  }
  
  static func helvetica14Light() -> UIFont? {
    return UIFont(name: "Helvetica Neue Light", size: 14)
  }
  
  static func helvetica17() -> UIFont? {
    return UIFont(name: "Helvetica Neue", size: 17)
  }
  
}
